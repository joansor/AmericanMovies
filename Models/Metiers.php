<?php

class Metiers extends Model
{
	#######################################################################################################
	#### CONSTRUCTEUR CONNEXION BDD #######################################################################
	#######################################################################################################

	public function __construct()
	{
		$this->pdo = parent::getPdo();
	}

	############################################################################################################
	#### METIERS ###############################################################################################
    ############################################################################################################

	########################################################################
	#### RETOURNE TOUS LES METIERS #########################################
	#### CATEGORIE ACTEURS ET REALISATEURS #################################
	########################################################################

	public function getAllMetiers()
	{
		$sql = 'SELECT * FROM metiers';
		$req = $this->pdo->prepare($sql);
		$req->execute();
		return $req->fetchAll();
	}







	
}